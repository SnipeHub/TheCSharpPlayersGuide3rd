using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace EqualSignMeaning
{
    class Program
    {
        static void Main(string[] args)
        {
            // The equal sign is an assignment operator, meaning that the stuff on the right gets calculated and put into the variable on the left side of the =.

            int a = 5;
            a = a + 1; // the variable a will have a value of 6 after running this line.

            // It's important to remember  that the '=' sign indicates assigment, rather than equality.
        }
    }
}
